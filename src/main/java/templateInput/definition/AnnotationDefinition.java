package templateInput.definition;

import java.util.HashMap;
import java.util.Map;

/**
 * Class for input to a template. Describes a parsed annotation. InputParameters to this annotations are represented by the extended hashmap.
 *
 * @author Daan
 */
// TODO probably best to not extend HashMap, but to implement a custom object wrapper:
// https://freemarker.apache.org/docs/pgui_datamodel_objectWrapper.html
public class AnnotationDefinition {

  /** The name of the annotation */
  private String name;

  /** The input parameters for the annotation */
  private Map<String, String> parameters = new HashMap<>();

  public AnnotationDefinition(String name) {
    super();
    this.name = name;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public Map<String, String> getParameters() {
    return parameters;
  }

  public void setParameters(Map<String, String> parameters) {
    this.parameters = parameters;
  }

  /**
   * This is used by the template engine and should only represent the name of the annotation
   */
  @Override
  public String toString() {
    return name;
  }

  @Override
  public int hashCode() {
    final int prime = 31;
    int result = 1;
    result = prime * result + ((name == null) ? 0 : name.hashCode());
    return result;
  }

  @Override
  public boolean equals(Object obj) {
    if (this == obj)
      return true;
    if (obj == null)
      return false;
    if (getClass() != obj.getClass())
      return false;
    AnnotationDefinition other = (AnnotationDefinition) obj;
    if (name == null) {
      if (other.name != null)
        return false;
    } else if (!name.equals(other.name))
      return false;
    return true;
  }

}
